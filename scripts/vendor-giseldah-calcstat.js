#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');

const GITHUB_OWNER = 'Giseldah';
const GITHUB_REPO = 'Giseldah.github.io';
const DEFAULT_REF = '77081c3ec33c5172f53a9cb15e565d3a7e84b3ac';
const TARGET_PATH = path.join(__dirname, '..', 'packages', 'discord', 'interact', 'vendor', 'giseldah-calcstat.js');

function resolveSourceRef(env) {
  const ref = String((env && env.GISELDAH_CALCSTAT_REF) || DEFAULT_REF).trim();
  if (!/^[0-9A-Za-z._/-]+$/.test(ref)) {
    throw new Error('Invalid GISELDAH_CALCSTAT_REF: ' + ref);
  }
  return ref;
}

function buildSourceUrl(ref) {
  return 'https://raw.githubusercontent.com/' + GITHUB_OWNER + '/' + GITHUB_REPO + '/' + ref + '/js/calcstat.js';
}

function buildVendoredFile(source, sourceUrl) {
  return [
    '// Generated by scripts/vendor-giseldah-calcstat.js',
    '// Source: ' + sourceUrl,
    '// Original work by Giseldah',
    '',
    source.trimEnd(),
    '',
    'exports.CalcStat = CalcStat;',
    'exports.DblCalcDev = DblCalcDev;',
    '',
  ].join('\n');
}

async function loadSource(options) {
  var opts = options || {};
  var sourceUrl = opts.sourceUrl || buildSourceUrl(resolveSourceRef(process.env));
  var targetPath = opts.targetPath || TARGET_PATH;
  var fetchImpl = opts.fetchImpl || fetch;
  var existsSync = opts.existsSync || fs.existsSync;

  try {
    const response = await fetchImpl(sourceUrl, {
      headers: { 'User-Agent': 'lotroguides.com build sync' },
    });
    if (!response.ok) {
      throw new Error('HTTP ' + response.status + ' while fetching ' + sourceUrl);
    }
    return {
      source: await response.text(),
      sourceLabel: sourceUrl,
    };
  } catch (err) {
    if (existsSync(targetPath)) {
      return {
        source: null,
        sourceLabel: targetPath,
        warning: err.message,
      };
    }

    throw err;
  }
}

async function main() {
  var sourceRef = resolveSourceRef(process.env);
  var sourceUrl = buildSourceUrl(sourceRef);
  var loaded = await loadSource({
    sourceUrl: sourceUrl,
  });

  if (!loaded.source) {
    console.warn('⚠ Failed to fetch Giseldah calcstat.js: ' + loaded.warning);
    console.warn('  Keeping existing vendored copy at ' + loaded.sourceLabel);
    return;
  }

  var output = buildVendoredFile(loaded.source, sourceUrl);
  var existing = fs.existsSync(TARGET_PATH) ? fs.readFileSync(TARGET_PATH, 'utf8') : '';

  fs.mkdirSync(path.dirname(TARGET_PATH), { recursive: true });

  if (existing === output) {
    console.log('✓ Giseldah calcstat.js already up to date (' + sourceUrl + ')');
    return;
  }

  fs.writeFileSync(TARGET_PATH, output, 'utf8');
  console.log('✓ Synced Giseldah calcstat.js from ' + sourceUrl);
}

exports.DEFAULT_REF = DEFAULT_REF;
exports.TARGET_PATH = TARGET_PATH;
exports.resolveSourceRef = resolveSourceRef;
exports.buildSourceUrl = buildSourceUrl;
exports.buildVendoredFile = buildVendoredFile;
exports.loadSource = loadSource;
exports.main = main;

if (require.main === module) {
  main().catch(function (err) {
    console.error('✗ Failed to sync Giseldah calcstat.js: ' + err.message);
    process.exit(1);
  });
}
